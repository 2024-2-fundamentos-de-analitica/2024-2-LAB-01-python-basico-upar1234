import csv

def pregunta_08():
    """
    Genere una lista de tuplas, donde el primer elemento de cada tupla
    contiene  el valor de la segunda columna; la segunda parte de la tupla
    es una lista con las letras (ordenadas y sin repetir letra) de la
    primera  columna que aparecen asociadas a dicho valor de la segunda
    columna.

    Rta/
    [(0, ['C']),
     (1, ['B', 'E']),
     (2, ['A', 'E']),
     (3, ['A', 'B', 'D', 'E']),
     (4, ['B', 'E']),
     (5, ['B', 'C', 'D', 'E']),
     (6, ['A', 'B', 'C', 'E']),
     (7, ['A', 'C', 'D', 'E']),
     (8, ['A', 'B', 'D', 'E']),
     (9, ['A', 'B', 'C', 'E'])]

    """
    csvfile = open("C:/Universidad/Sistemas/Fundamentos de anal√≠tica/Labs/2024-2-LAB-01-python-basico-upar1234/files/input/data.csv", "r")
    reader = csv.reader(csvfile)
    l = []
    for i in range(0, 10):
        l.append((i, []))
    for line in reader:
        line = str(line[0]).split()
        line = line[0]+line[1]
        for element in l:
            if int(line[1]) == element[0]:
                if line[0] not in element[1]:
                    element[1].append(line[0])
            element[1].sort()
    return l

